/* Add, Subtract, Multiply, Division (+, -, *, /) using Arrow Function */

let calc = (num1:number, num2:number) => {
    console.log(num1+num2);
    console.log(num1-num2);
    console.log(num1*num2);
    console.log(num1/num2);
}

calc(10,50);



